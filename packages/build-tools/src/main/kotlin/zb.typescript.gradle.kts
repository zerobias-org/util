/**
 * Read module name and version from package.json using regex.
 * Returns (name, version) or throws if not found.
 */
fun readPackageNameVersion(): Pair<String, String> {
    val pkgJson = project.file("package.json")
    require(pkgJson.exists()) { "package.json not found in ${project.projectDir}" }
    val content = pkgJson.readText()
    val name = Regex(""""name"\s*:\s*"([^"]+)"""").find(content)?.groupValues?.get(1)
        ?: throw org.gradle.api.GradleException("Cannot find 'name' in package.json")
    val version = Regex(""""version"\s*:\s*"([^"]+)"""").find(content)?.groupValues?.get(1)
        ?: throw org.gradle.api.GradleException("Cannot find 'version' in package.json")
    return name to version
}

/**
 * Run a CLI command, capture stdout, and parse it as JSON array or object.
 * Returns the raw JSON string for caller to parse.
 * Uses slot env (already in process.env via ZbbSlotProvider).
 */
fun runCliJson(vararg command: String): String {
    return com.zerobias.buildtools.util.ExecUtils.execCapture(
        command = command.toList(),
        workingDir = project.projectDir,
        environment = emptyMap(), // inherits Gradle process env (slot vars already set)
        throwOnError = true
    ).trim()
}

// Optional: test against a published module instead of local build
// Usage: zbb testHub -PhubTestModule=@auditlogic/module-github-github@6.8.0
val hubTestModule: String? = project.findProperty("hubTestModule") as? String

val setupFixtures by tasks.registering {
    group = "lifecycle"
    description = "Set up Hub e2e test fixtures: dataloader, deployment, connection"
    if (hubTestModule == null) {
        dependsOn(tasks.named("compile"))
    }
    onlyIf { project.file("test/e2e").exists() }
    doLast {
        // Resolve slot env (needed for CLI commands that read SERVER_URL, API_KEY, etc.)
        com.zerobias.buildtools.util.ZbbSlotProvider.requireActiveSlot()

        val moduleKey: String
        val moduleVersion: String
        if (hubTestModule != null) {
            // Parse published module: @scope/name@version
            val atIndex = hubTestModule.lastIndexOf('@')
            if (atIndex <= 0) {
                throw org.gradle.api.GradleException(
                    "Invalid hubTestModule format: $hubTestModule. Expected: @scope/name@version"
                )
            }
            moduleKey = hubTestModule.substring(0, atIndex)
            moduleVersion = hubTestModule.substring(atIndex + 1)
            logger.lifecycle("setupFixtures: Testing published module $moduleKey@$moduleVersion")
        } else {
            val (key, _) = readPackageNameVersion()
            moduleKey = key
            moduleVersion = project.version.toString()
        }
        logger.lifecycle("setupFixtures: $moduleKey@$moduleVersion")

        // ── Step 1: Load module artifacts via dataloader ───────────────────
        if (hubTestModule != null) {
            logger.lifecycle("setupFixtures: Step 1 — loading published module via dataloader")
            com.zerobias.buildtools.util.ExecUtils.exec(
                command = listOf("zbb", "dataloader", moduleKey, moduleVersion),
                workingDir = project.projectDir,
                throwOnError = true
            )
        } else {
            // Dataloader expects module-{name}.yml in module root; we generate it to generated/.
            // Symlink so dataloader finds it without duplicating the file.
            val noScope = moduleKey.split("/").last()
            val distSpec = project.file("generated/${noScope}.yml")
            val rootLink = project.file("${noScope}.yml")
            if (distSpec.exists() && !rootLink.exists()) {
                java.nio.file.Files.createSymbolicLink(rootLink.toPath(), distSpec.toPath())
            }
            logger.lifecycle("setupFixtures: Step 1 — loading module artifacts via zbb dataloader")
            com.zerobias.buildtools.util.ExecUtils.exec(
                command = listOf("zbb", "dataloader", "-d", "."),
                workingDir = project.projectDir,
                throwOnError = true
            )
        }

        // ── Step 2: Get registered node ID (auto-register + start if needed) ─
        logger.lifecycle("setupFixtures: Step 2 — getting registered node ID")
        var nodeListJson = runCliJson("hub-node", "--json", "server", "node", "list")
        var nodeId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(nodeListJson)?.groupValues?.get(1)

        if (nodeId == null) {
            // No node registered — register via CLI, set code
            logger.lifecycle("setupFixtures: No node found — auto-registering")
            val registerJson = runCliJson(
                "hub-node", "--json", "server", "register", "--max-nodes", "1"
            )
            val regCode = Regex(""""code"\s*:\s*"([^"]+)"""").find(registerJson)?.groupValues?.get(1)
                ?: throw org.gradle.api.GradleException(
                    "setupFixtures: No registration code in register output.\n$registerJson"
                )
            logger.lifecycle("setupFixtures: Registration code = $regCode")

            // Set registration code in slot env so node picks it up on start
            com.zerobias.buildtools.util.ExecUtils.exec(
                command = listOf("hub-node", "env", "set", "REGISTRATION_CODE", regCode),
                workingDir = project.projectDir,
                throwOnError = true
            )
        }

        // Always ensure node is running (idempotent — returns immediately if already up)
        logger.lifecycle("setupFixtures: Ensuring node is running")
        com.zerobias.buildtools.util.ExecUtils.exec(
            command = listOf("hub-node", "node", "start"),
            workingDir = project.projectDir,
            throwOnError = true
        )

        if (nodeId == null) {
            // Wait for freshly registered node to appear in server
            logger.lifecycle("setupFixtures: Waiting for node to register...")
            var retries = 0
            while (retries < 30) {
                Thread.sleep(2000)
                nodeListJson = runCliJson("hub-node", "--json", "server", "node", "list")
                nodeId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(nodeListJson)?.groupValues?.get(1)
                if (nodeId != null) break
                retries++
                if (retries % 5 == 0) {
                    logger.lifecycle("setupFixtures: Still waiting for node registration... (${retries * 2}s)")
                }
            }
            if (nodeId == null) {
                throw org.gradle.api.GradleException(
                    "setupFixtures: Node did not register after 60s.\n" +
                    "Check hub-node logs: zbb logs show node"
                )
            }
        }
        logger.lifecycle("setupFixtures: Node ID = $nodeId")

        // ── Step 3: Find or create deployment ─────────────────────────────
        logger.lifecycle("setupFixtures: Step 3 — deployment for $moduleKey@$moduleVersion on node $nodeId")
        val existingDeployJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
            command = listOf("hub-node", "--json", "server", "deployments", "list"),
            workingDir = project.projectDir,
            environment = emptyMap(),
            throwOnError = false
        ).trim()
        var deploymentId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(existingDeployJson)?.groupValues?.get(1)
        if (deploymentId != null) {
            // Check if deployment version matches — update if not
            val existingVersion = Regex(""""moduleVersion"\s*:\s*"([^"]+)"""").find(existingDeployJson)?.groupValues?.get(1)
            if (existingVersion != null && existingVersion != moduleVersion) {
                logger.lifecycle("setupFixtures: Deployment version mismatch ($existingVersion → $moduleVersion), updating...")
                runCliJson(
                    "hub-node", "--json", "deployments", "update", deploymentId,
                    "--module", "$moduleKey@$moduleVersion"
                )
                logger.lifecycle("setupFixtures: Updated Deployment ID = $deploymentId to $moduleKey@$moduleVersion")
            } else {
                logger.lifecycle("setupFixtures: Reusing Deployment ID = $deploymentId")
            }
            // Clear stale local slot state so the node re-ensures the deployment fresh
            val slotDir = System.getenv("ZB_SLOT_DIR")
            if (slotDir != null) {
                val imageName = moduleKey.substringAfter("/").replace("/", "-")
                val depStateDir = java.io.File("$slotDir/state/deployments/$imageName/$deploymentId")
                if (depStateDir.exists()) {
                    depStateDir.deleteRecursively()
                    logger.lifecycle("setupFixtures: Cleared stale local deployment state")
                }
            }
        } else {
            logger.lifecycle("setupFixtures: Creating deployment")
            val deployJson = runCliJson(
                "hub-node", "--json", "deployments", "create",
                "--module", "$moduleKey@$moduleVersion",
                "--node-id", nodeId
            )
            deploymentId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(deployJson)?.groupValues?.get(1)
                ?: throw org.gradle.api.GradleException(
                    "setupFixtures: No deployment ID in output.\n$deployJson"
                )
            logger.lifecycle("setupFixtures: Created Deployment ID = $deploymentId")
        }

        // ── Step 4: Get boundary ID ─────────────────────────────────────────
        logger.lifecycle("setupFixtures: Step 4 — getting boundary ID")
        val boundaryListJson = runCliJson("hub-node", "--json", "server", "boundaries", "list")
        val boundaryId = run {
            val idMatch = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(boundaryListJson)
                ?: throw org.gradle.api.GradleException(
                    "setupFixtures: No boundary ID in output.\n$boundaryListJson"
                )
            idMatch.groupValues[1]
        }
        logger.lifecycle("setupFixtures: Boundary ID = $boundaryId")

        // ── Step 5: Find or create connection ────────────────────────────────
        logger.lifecycle("setupFixtures: Step 5 — connection for deployment $deploymentId")

        // Check for existing connection on this deployment
        val existingConnJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
            command = listOf("hub-node", "--json", "connections", "list", "--deployment", deploymentId),
            workingDir = project.projectDir,
            environment = emptyMap(),
            throwOnError = false
        ).trim()
        var targetId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(existingConnJson)?.groupValues?.get(1)

        if (targetId != null) {
            logger.lifecycle("setupFixtures: Reusing Connection (TARGET_ID) = $targetId")
        } else {
            // Find zbb secret name for this module
            val secretListJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
                command = listOf("zbb", "secret", "list", "--module", moduleKey, "--json"),
                workingDir = project.projectDir,
                environment = emptyMap(),
                throwOnError = false
            ).trim()

            // Parse secret name and _id from JSON array: [{"name":"github","module":"...","_id":"uuid"}]
            val secretName = Regex(""""name"\s*:\s*"([^"]+)"""").find(secretListJson)?.groupValues?.get(1)
            var secretHubId = Regex(""""_id"\s*:\s*"([0-9a-f-]{36})"""").find(secretListJson)?.groupValues?.get(1)

            // If we have a zbb secret but no Hub server ID, create the Hub secret
            if (secretName != null && secretHubId == null) {
                logger.lifecycle("setupFixtures: Creating Hub secret for zbb secret '$secretName'")

                // Read zbb secret keys to build file-ref profile
                val secretGetJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
                    command = listOf("zbb", "secret", "get", secretName, "--json"),
                    workingDir = project.projectDir,
                    environment = emptyMap(),
                    throwOnError = true
                ).trim()
                // Extract keys from JSON object (skip metadata)
                val profileKeys = Regex(""""([^"_][^"]*?)"\s*:""").findAll(secretGetJson)
                    .map { it.groupValues[1] }.toList()
                val profileJson = profileKeys.joinToString(",") { """"$it":"file.$secretName.$it"""" }

                // Get connectionProfileId from deployment
                val deployGetJson = runCliJson("hub-node", "--json", "server", "deployments", "get", deploymentId)
                val connProfileId = Regex(""""connectionProfileId"\s*:\s*"([0-9a-f-]{36})"""")
                    .find(deployGetJson)?.groupValues?.get(1)
                    ?: throw org.gradle.api.GradleException(
                        "setupFixtures: No connectionProfileId on deployment.\n$deployGetJson"
                    )

                // Create Hub secret with file refs
                val createSecretJson = runCliJson(
                    "hub-node", "--json", "server", "secrets", "create",
                    "--name", "$secretName (file)",
                    "--connection-profile-id", connProfileId,
                    "--boundary-id", boundaryId,
                    "--profile", "{$profileJson}"
                )
                secretHubId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(createSecretJson)?.groupValues?.get(1)
                    ?: throw org.gradle.api.GradleException(
                        "setupFixtures: No secret ID in create output.\n$createSecretJson"
                    )
                logger.lifecycle("setupFixtures: Created Hub secret $secretHubId")

                // Save _id back to zbb secret file for next run
                com.zerobias.buildtools.util.ExecUtils.exec(
                    command = listOf("zbb", "secret", "update", secretName, "_id=$secretHubId"),
                    workingDir = project.projectDir,
                    throwOnError = true
                )
                logger.lifecycle("setupFixtures: Saved Hub secret ID to zbb secret '$secretName'")
            }

            val moduleName = moduleKey.substringAfterLast('/').split('-').last().replaceFirstChar { it.uppercase() }
            val connectionName = "$moduleName E2E"

            val connArgs = mutableListOf(
                "hub-node", "--json", "connections", "create",
                "--deployment-id", deploymentId,
                "--boundary-id", boundaryId,
                "--name", connectionName,
                "--mode", "auto"
            )
            if (secretHubId != null) {
                logger.lifecycle("setupFixtures: Using Hub secret $secretHubId")
                connArgs.addAll(listOf("--secret-id", secretHubId))
            }

            val connJson = runCliJson(*connArgs.toTypedArray())
            targetId = Regex(""""id"\s*:\s*"([0-9a-f-]{36})"""").find(connJson)?.groupValues?.get(1)
                ?: throw org.gradle.api.GradleException(
                    "setupFixtures: No connection ID in output.\n$connJson"
                )
            logger.lifecycle("setupFixtures: Created Connection (TARGET_ID) = $targetId")
        }

        // ── Step 5b: Always sync zbb secret → Hub secret ─────────────────
        // Ensures the Hub secret profile matches the local zbb secret (handles
        // first run with empty draft secret AND subsequent token rotations).
        val syncSecretListJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
            command = listOf("zbb", "secret", "list", "--module", moduleKey, "--json"),
            workingDir = project.projectDir,
            environment = emptyMap(),
            throwOnError = false
        ).trim()
        val syncSecretName = Regex(""""name"\s*:\s*"([^"]+)"""").find(syncSecretListJson)?.groupValues?.get(1)

        if (syncSecretName != null) {
            // Get secretId from the connection on this deployment
            val connListJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
                command = listOf("hub-node", "--json", "connections", "list", "--deployment", deploymentId),
                workingDir = project.projectDir,
                environment = emptyMap(),
                throwOnError = false
            ).trim()
            val hubSecretId = Regex(""""secretId"\s*:\s*"([0-9a-f-]{36})"""").find(connListJson)?.groupValues?.get(1)

            if (hubSecretId != null) {
                val secretGetJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
                    command = listOf("zbb", "secret", "get", syncSecretName, "--json"),
                    workingDir = project.projectDir,
                    environment = emptyMap(),
                    throwOnError = true
                ).trim()

                // Build profile JSON from zbb secret (skip metadata keys starting with _)
                val profileEntries = Regex(""""([^"_][^"]*?)"\s*:\s*"([^"]*?)"""").findAll(secretGetJson)
                    .map { """"${it.groupValues[1]}":"${it.groupValues[2]}"""" }
                    .joinToString(",")
                val profileJson = "{$profileEntries}"

                runCliJson(
                    "hub-node", "--json", "server", "secrets", "update", hubSecretId,
                    "--profile", profileJson,
                    "--draft", "false"
                )
                logger.lifecycle("setupFixtures: Synced secret $hubSecretId from zbb secret '$syncSecretName'")
            } else {
                logger.warn("setupFixtures: No secretId found on connection — secret sync skipped")
            }
        }

        // ── Step 6: Ensure deployment + connect via target metadata ──────
        // getTargetMetadata ensures deployment is running and connection is established
        logger.lifecycle("setupFixtures: Step 6 — ensuring target ready (TARGET_ID=$targetId)")
        val metadataJson = com.zerobias.buildtools.util.ExecUtils.execCapture(
            command = listOf("hub-node", "--json", "server", "targets", "metadata", targetId),
            workingDir = project.projectDir,
            throwOnError = true
        ).trim()
        // hub-node may exit 0 but return a hub-level error in JSON
        if (metadataJson.contains("\"err.not.found\"") || metadataJson.contains("\"statusCode\":404") || metadataJson.contains("\"statusCode\": 404")) {
            throw org.gradle.api.GradleException(
                "setupFixtures: Target not ready — deployment not found on node.\n$metadataJson\n" +
                "The node may not have this deployment loaded. Try: zbb destroy && zbb up"
            )
        }

        // Export fixture IDs for testHub task
        project.ext.set("SETUP_FIXTURES_TARGET_ID", targetId)
        project.ext.set("SETUP_FIXTURES_DEPLOYMENT_ID", deploymentId)
        logger.lifecycle("setupFixtures: complete. TARGET_ID=$targetId DEPLOYMENT_ID=$deploymentId")
    }
}

