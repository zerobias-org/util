// G4: bundleSpec — inline all $ref entries via Redocly CLI
val bundleSpec by tasks.registering(NpxTask::class) {
    group = "lifecycle"
    description = "Bundle all \$ref entries into self-contained spec"
    dependsOn(assembleSpec, npmInstallModule)
    workingDir.set(project.projectDir)
    command.set("@redocly/cli")
    args.set(listOf("bundle", assembledSpec.name, "--output", bundledSpec.name))
    // Run from generated/ — $ref resolves relative to input file
    workingDir.set(project.file("generated"))
    inputs.file(assembledSpec)
    inputs.dir("node_modules")
    outputs.file(bundledSpec)
    doFirst {
        // Copy everything $ref needs into generated/ so resolution works
        val genDir = project.file("generated")
        project.file("node_modules").let { nm ->
            val link = java.io.File(genDir, "node_modules")
            if (!link.exists()) java.nio.file.Files.createSymbolicLink(link.toPath(), nm.toPath())
        }
        listOf("connectionProfile.yml", "connectionState.yml", "api.yml").forEach { name ->
            val src = project.file(name)
            if (src.exists()) src.copyTo(java.io.File(genDir, name), overwrite = true)
        }
    }
}

