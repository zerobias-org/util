@file:OptIn(ExperimentalStdlibApi::class)

import com.github.gradle.node.npm.task.NpmTask
import com.zerobias.buildtools.content.SchemaPrimitives
import com.zerobias.buildtools.schema.SchemaTsGenerator
import com.zerobias.buildtools.schema.TsTemplatePatcher
import com.zerobias.buildtools.tasks.NeonBranchContext
import com.zerobias.buildtools.tasks.NeonDataloaderTask

/**
 * zb.schema — AuditgraphDB schema packages.
 *
 * Extends `zb.content` with a TypeScript-twin publish pipeline. Each
 * schema package ships TWO npm packages at the same version:
 *
 *   @<scope>/schema-<v>-<p>      ← YAML schema definitions (zb.content)
 *   @<scope>/schema-<v>-<p>-ts   ← TypeScript interfaces generated
 *                                  from the loaded schema
 *
 * The TS twin is generated AT GATE TIME, inside the same Neon branch
 * that runs dataloader. That's the only place where the schema is
 * loaded and queryable by `schema-ts-generator`, so generation has to
 * happen while the branch is alive. We attach the generator to
 * `testIntegrationDataloader.postLoadActions`, which the upstream task
 * invokes after dataloader succeeds and before the branch tears down.
 *
 * Per-package marker is one line:
 *   plugins { id("zb.schema") }
 *
 * Failure semantics — if TS twin publish fails after the schema package
 * already published:
 *   - `publishNpm` aggregate fails (it depends on both schema +
 *     twin publish).
 *   - `promoteAll` therefore doesn't run; both packages stay on the
 *     `next` dist-tag only.
 *   - Re-running publish is idempotent on the schema side (already
 *     published → skips) and retries the twin. When both succeed,
 *     `promoteAll` runs and both move tags together.
 *
 * Deprecated schemas (zerobias.deprecated == true) skip TS gen and
 * skip TS twin publish entirely — the YAML side still publishes
 * normally so dataloader can record the deprecation.
 */

plugins {
    id("zb.content")
}

// ════════════════════════════════════════════════════════════
// GATE — hook TS generation into the existing Neon dataloader task.
// ════════════════════════════════════════════════════════════
//
// `zb.content` registered `testIntegrationDataloader` (NeonDataloaderTask).
// We attach a post-load action: after dataloader succeeds and while the
// branch is still alive, run schema-ts-generator + tsc and produce
// <pkg>/ts/dist/. Failure throws → the task fails → the gate fails.

val schemaTsGenAction = object : org.gradle.api.Action<NeonBranchContext> {
    override fun execute(ctx: NeonBranchContext) {
        SchemaTsGenerator.generate(ctx)
    }
}

tasks.named<NeonDataloaderTask>("testIntegrationDataloader") {
    postLoadActions.add(schemaTsGenAction)
}

// ════════════════════════════════════════════════════════════
// PUBLISH — TS twin npm publish, hung off the schema publish.
// ════════════════════════════════════════════════════════════
//
// The TS twin lives in <pkg>/ts/. Generated during gate; published
// here. Same `--tag next` + promote flow that `zb.content` uses for
// the schema package, so both move dist-tags in lockstep.

val isDryRunTs: Boolean = extra["isDryRun"] as Boolean
@Suppress("UNCHECKED_CAST")
val npmDistTagsTs: List<String> = extra["npmDistTags"] as List<String>
@Suppress("UNCHECKED_CAST")
val stagedPackagesTs = extra["stagedPackages"] as MutableList<Pair<String, java.io.File>>

fun readTsArtifactName(): String {
    val pkgDoc = SchemaPrimitives.parseJson(project.file("package.json"))
    val schemaName = (pkgDoc["name"] as? String)
        ?: throw GradleException("[zb.schema] package.json missing 'name'")
    return "$schemaName-ts"
}

fun tsDir(): java.io.File = project.file("ts")

fun isTsTwinSkipped(): Boolean {
    val pkgDoc = SchemaPrimitives.parseJson(project.file("package.json"))
    if (TsTemplatePatcher.isDeprecated(pkgDoc)) return true
    return !tsDir().isDirectory
}

// ── Patch ts/package.json with project.version (resolved at publish time) ──

var originalTsPackageJson: String? = null
var originalTsPackageLock: String? = null

val patchTsPackageJson by tasks.registering {
    group = "publish"
    description = "Patch ts/package.json + ts/package-lock.json with resolved version"
    onlyIf { !isTsTwinSkipped() }
    doLast {
        val ver = project.version.toString()
        val pkgFile = tsDir().resolve("package.json")
        val lockFile = tsDir().resolve("package-lock.json")
        if (!pkgFile.isFile) {
            throw GradleException(
                "[zb.schema] ts/package.json missing — was the gate run? " +
                "TS twin is generated by testIntegrationDataloader during gate."
            )
        }
        originalTsPackageJson = pkgFile.readText()
        pkgFile.writeText(
            originalTsPackageJson!!.replace(
                Regex(""""version"\s*:\s*"[^"]+""""),
                """"version": "$ver""""
            )
        )
        if (lockFile.isFile) {
            originalTsPackageLock = lockFile.readText()
            // Lockfile carries the project version at the top level and
            // inside packages."".version. Targeted patch — never touch
            // the nested dependency versions.
            val patched = com.zerobias.buildtools.schema.TsTemplatePatcher
                .patchPackageLockVersion(originalTsPackageLock!!, ver)
            lockFile.writeText(patched)
        }
        logger.lifecycle("[zb.schema] Patched ts/package.json version to $ver")
    }
}

val restoreTsPackageJson by tasks.registering {
    group = "publish"
    description = "Restore ts/package.json + ts/package-lock.json after publish"
    onlyIf { !isTsTwinSkipped() }
    doLast {
        originalTsPackageJson?.let { tsDir().resolve("package.json").writeText(it) }
        originalTsPackageLock?.let { tsDir().resolve("package-lock.json").writeText(it) }
    }
}

val stageTsShrinkwrap by tasks.registering {
    group = "publish"
    description = "Copy ts/package-lock.json → ts/npm-shrinkwrap.json so the tarball ships a lockfile"
    onlyIf { !isTsTwinSkipped() }
    doLast {
        val lock = tsDir().resolve("package-lock.json")
        val shrink = tsDir().resolve("npm-shrinkwrap.json")
        if (lock.exists()) {
            lock.copyTo(shrink, overwrite = true)
            logger.lifecycle("[zb.schema] Staged ts/npm-shrinkwrap.json from ts/package-lock.json")
        }
    }
}

val cleanupTsShrinkwrap by tasks.registering {
    group = "publish"
    description = "Remove ephemeral ts/npm-shrinkwrap.json after publish"
    onlyIf { !isTsTwinSkipped() }
    doLast {
        val shrink = tsDir().resolve("npm-shrinkwrap.json")
        if (shrink.exists() && shrink.delete()) {
            logger.lifecycle("[zb.schema] Removed ephemeral ts/npm-shrinkwrap.json")
        }
    }
}

fun isTsAlreadyPublished(name: String, version: String, workDir: java.io.File): Boolean {
    return try {
        val output = com.zerobias.buildtools.util.ExecUtils.exec(
            command = listOf("npm", "view", "$name@$version", "version"),
            workingDir = workDir,
            throwOnError = false,
            captureOutput = true,
        ).trim()
        output == version
    } catch (_: Exception) {
        false
    }
}

val publishTsTwin by tasks.registering(NpmTask::class) {
    group = "publish"
    description = "Publish the TypeScript twin package (@<scope>/schema-<v>-<p>-ts) to npm"
    onlyIf {
        if (isTsTwinSkipped()) {
            logger.lifecycle(
                "[zb.schema] TS twin skipped (deprecated schema or ts/ missing) for ${project.path}"
            )
            false
        } else true
    }

    // Run AFTER the schema package itself published. If schema publish
    // fails, no point trying the twin.
    dependsOn(tasks.named("publishNpmExec"))
    dependsOn(patchTsPackageJson, stageTsShrinkwrap)
    finalizedBy(restoreTsPackageJson, cleanupTsShrinkwrap)

    npmCommand.set(listOf("publish"))
    args.set(listOf("--tag", "next"))
    workingDir.set(tsDir())

    doFirst {
        if (isTsTwinSkipped()) return@doFirst
        val name = readTsArtifactName()
        val ver = project.version.toString()
        if (isDryRunTs) {
            logger.lifecycle("[DRY RUN] Would publish $name@$ver --tag next")
            throw org.gradle.api.tasks.StopExecutionException()
        }
        if (isTsAlreadyPublished(name, ver, tsDir())) {
            logger.lifecycle("[zb.schema] $name@$ver already published — skipping (will still promote)")
            stagedPackagesTs.add(name to tsDir())
            throw org.gradle.api.tasks.StopExecutionException()
        }
    }
    doLast {
        if (isTsTwinSkipped()) return@doLast
        val name = readTsArtifactName()
        stagedPackagesTs.add(name to tsDir())
    }
}

// ── Hook TS twin into the publishNpm aggregate ──
// If publishTsTwin fails, publishNpm fails. zbb publish lifecycle won't
// proceed to promoteAll, so both schema + twin stay on `next`. (User
// answer 7-C: TS twin failure aborts promotion of the schema too.)
tasks.named("publishNpm") {
    dependsOn(publishTsTwin)
}

// ── Promote the TS twin in lockstep with the schema package ──

val promoteTsTwin by tasks.registering {
    group = "publish"
    description = "Promote the TS twin from 'next' to all applicable dist-tags"
    onlyIf { !isTsTwinSkipped() }
    doLast {
        val name = readTsArtifactName()
        val ver = project.version.toString()
        if (isDryRunTs) {
            logger.lifecycle("[DRY RUN] Would promote $name@$ver to tags: ${npmDistTagsTs.joinToString(", ")}")
            return@doLast
        }
        for (tag in npmDistTagsTs) {
            logger.lifecycle("[zb.schema]   Tagging $name@$ver → $tag")
            com.zerobias.buildtools.util.ExecUtils.exec(
                command = listOf("npm", "dist-tag", "add", "$name@$ver", tag),
                workingDir = tsDir(),
                throwOnError = true,
            )
        }
        try {
            com.zerobias.buildtools.util.ExecUtils.exec(
                command = listOf("npm", "dist-tag", "rm", name, "next"),
                workingDir = tsDir(),
                throwOnError = false,
            )
        } catch (_: Exception) {}
    }
}

tasks.named("promoteAll") {
    dependsOn(promoteTsTwin)
}
